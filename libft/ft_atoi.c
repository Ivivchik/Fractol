#include "libft.h"

int		ft_atoi(const char *s)
{
	int		i;
	long	n;
	int		sign;

	i = 0;
	n = 0;
	sign = 1;
	while (s[i] == ' ' || s[i] == '\t' || s[i] == '\n'
			|| s[i] == '\v' || s[i] == '\f' || s[i] == '\r')
		i++;
	if (s[i] == '-')
		sign = -1;
	if (s[i] == '+' || s[i] == '-')
		i++;
	while (s[i] != '\0' && s[i] >= '0' && s[i] <= '9')
	{
		n = n * 10 + s[i] - '0';
		if (n < 0)
			return (sign == -1 ? 0 : -1);
		i++;
	}
	return (n * sign);
}
