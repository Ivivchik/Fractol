#include "libft.h"

char	*ft_strcat(char *dst, const char *src)
{
	size_t	i;
	int		j;

	i = ft_strlen(dst);
	j = 0;
	while (src[j] != '\0')
	{
		dst[j + i] = src[j];
		j++;
	}
	dst[j + i] = '\0';
	return (dst);
}
