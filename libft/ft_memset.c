#include "libft.h"

void	*ft_memset(void *d, int c, size_t n)
{
	size_t	i;
	char	*a;

	i = 0;
	a = (char *)d;
	while (i < n)
	{
		a[i] = c;
		i++;
	}
	return (d);
}
